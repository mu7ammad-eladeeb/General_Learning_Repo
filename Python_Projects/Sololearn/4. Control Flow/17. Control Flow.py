# Computers are capable of making decisions, and help us solve real-world problems because they are very good at following instructions.
# In this lesson, you'll learn to control the flow of instructions a computer follows, using 3 techinques: sequencing, iteration and selection.
# You already know sequencing. It means that the computer runs your code in order, from top to bottom.
# Iteration is about executing an instruction repeatedly. Iteration is commonly represented as a loop.
# Putting your favorite song on repeat is an example of iteration.
# Selection specifies when to follow each path.
# Smartwatches notify the wearer if their heart rate goes outside the normal range. This is an example of selection.
# Real computer programs perform complex tasks by combining the 3 techniques.
# Machines can complete complex tasks for us, but first they need to know how.
# An algorithm is a set of step by step insructions to compelte a task, placed in a certain order.
# Algorithms exist in our everyday lives.
# The step-by-step instructions in a cooking recipe are an example of an algorithm.
# Algorithms can be represented in many ways. For example flowcharts help to visualize algorithms.
 
# Another way to represnet an algorithm is with pseudocode. Pseudocode is a simplified language that is a bit closer to a programming language: 
# start
# repeat until authorised = true
#   InPUT log_in
# access_granted = true
# It is not specific to one programming language.
# Algorithms can be represented in different ways. If you are new to algorithms, natural language is a good place to start.
# Directions to a place and cooking recipe are examples of algorithms.
# Humans use code to communicate with machines. For machines to complete a task, the instructions need to be free of errors, given in the right order and given in a language that the machine can understand.
