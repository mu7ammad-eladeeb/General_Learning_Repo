# A function can be an argument for another function:
print(type("word"))
# type("word") is an argument of print()
print(int(5.5)) # 5
print(int('5')) # 5
print(str(5)) # 5
print(int('pencil')) # error
# Awesome! You have learned that:
# You can use variables as arguments for functions.
# A function can be an argument for another function.
# Some functions require specific data types as arguments like range(5(stop)) or range(5(start),3(stop)) or range(3(start),4(stop),2(step)) requires integers only.
