# Lists are incredibly useful in programmin! They let you store multiple values in a single variable, making your code more efficient.
# In this lesson, you'll learn to work with data stored in lists.
# Values in a list must be separated by commas.
# You can refer to values in a list using their position (or index).
# The following code is used to store the value "dog" in a variable called my_pet
animals = ["cat", "dog", "bird"]
my_pet = animals[1]
# Lists are mutable. This means you can change their values even after they've been created as following:
nums = [8,6,19]
nums[0] = 1
nums[1] = 2
nums[2] = 3
print(nums)
