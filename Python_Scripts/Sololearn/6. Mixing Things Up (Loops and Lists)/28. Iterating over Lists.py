# You can automate and speed up the procesing of a large amount of data if you know how to iterate over a list.
# In this lesson, you'll learn to use loops to go through each element of a list.
# You can check if an item is in a particular list by using the in operator.
products = ['milk', 'eggs', 'apples']
print('bread' in products)  # False
# The in operator also works with strings.
word = 'movie'
print('o' in word) # True
